package Unidad4;

public class Clase1 {
    public static void main(String[] args) {
        int i = 2;

        switch(i) {
            case 0:
                System.out.println("i es cero.");
                break;
            case 1:
                System.out.println("i es uno.");
                break;
            case 2:
                System.out.println("i es dos.");
                break;
            case 3:
                System.out.println("i es tres.");
                break;
            default:
                System.out.println("i es mayor a tres.");
        }
    }
}
